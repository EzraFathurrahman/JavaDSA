public class Main {
    public static void main(String[] args) {
        int[] input = { 1, 2, 3, 4 };

        for (int i = 0; i < input.length; i++) {
            for (int j = 0; j < input[i]; i++) {
                System.out.println(input[i]);
            }
        }
    }
}
